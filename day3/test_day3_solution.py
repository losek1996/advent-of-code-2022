from day3.day3_solution import sum_priorities_1st_strategy, sum_priorities_2nd_strategy


def test_sum_priorities_1st_strategy():
    assert (
        sum_priorities_1st_strategy(
            words=[
                "vJrwpWtwJgWrhcsFMMfFFhFp",
                "jqHRNqRjqzjGDLGLrsFMfFZSrLrFZsSL",
                "PmmdzqPrVvPwwTWBwg",
                "wMqvLMZHhHMvwLHjbvcjnnSBnvTQFn",
                "ttgJtRGJQctTZtZT",
                "CrZsJsPPZsGzwwsLwLmpwMDw",
            ]
        )
        == 157
    )


